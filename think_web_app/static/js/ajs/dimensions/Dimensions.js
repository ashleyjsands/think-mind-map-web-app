/**
 * @fileoverview The Dimension class.
 */


goog.provide('ajs.Dimensions');


/**
 * The Dimensions class represents the dimensions of a rectangle.
 * 
 * @constructor
 */
ajs.Dimensions = function(x, y, width, height) {
  this.x = x;
  this.y = y;
  this.width = width;
  this.height = height;
}